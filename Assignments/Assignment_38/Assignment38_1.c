void strCpyRev(char *str, char *dest)
{
    char *start = str;
    int len = 0;

    while (*str != '\0')
    {
        str++;
        len++;
    }
    str--;

    while (len > 0)
    {
        *dest = *str;
        dest++;
        str--;
        len--;
    }
    *dest = '\0';

} // End of function

