#include<stdio.h>

////////////////////////////////////////////////////////////////////
//
//  Function Name   :   FindLarge
//  Description     :   Function to check whether which number is
//                      bigger among two.
//  Input           :   Integer
//  Output          :   Integer
//  Name of Author  :   Sahil Rajaram Thorat
//  Date            :   10/02/2026
