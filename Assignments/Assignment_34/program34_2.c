#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Toggles the case of a character. Lowercase becomes uppercase
//                  and uppercase becomes lowercase. Other characters remain same.
//  Input         : Character
//  Output        : Void
//  Author        : Sahil Rajaram Thorat
//  Date          : 29/11/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    if(ch >= 'a' && ch <='z')
    {
        ch = ch - 32;
    }
    else if(ch >= 'A' && ch <='Z')
    {
        ch = ch + 32;
    }

    printf("%c\n",ch);
    
}// End of Display Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter the character : \n");
    scanf("%c",&cValue);

    Display(cValue);

    return 0;
}// End of main Function

