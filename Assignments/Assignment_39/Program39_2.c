#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints 1    2   3   4   5  using recursion.
//  Input         : None
//  Output        : None
//  Author        : Sahil Rajaram Thorat
//  Date          : 06/12/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static int iCount = 0;     // Counter for recursive calls

    if(iCount < 5)            
    {
        printf("%d\t", iCount + 1);
        iCount++;
        Display();             // Recursive call
    }

} // End of Display

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();                

    return 0;

} // End of main

