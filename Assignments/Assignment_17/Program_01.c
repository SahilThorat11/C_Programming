//  Function Name   :   SumDigits
//  Description     :   Function to calculate sum of digits from 
//                      given number.
//  Input           :   Integer
//  Output          :   Integer
//  Name of Author  :   Sahil Rajaram Thorat
//  Date            :   10/02/2026
//
