void Table(int iNo)
{
    int iCnt = 0, iTable = 1;

    for(iCnt = 1; iCnt <= 10; iCnt++)
    {
        iTable = iNo * iCnt;
        printf("%d\t", iTable);
    }
    
} // End of Table Function

////////////////////////////////////////////////////////////////////
//
// Entry point Function of Application
//
////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    Table(iValue);

    return 0;
} // End of main Function

