////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Strlen
//  Description   : Accept String and count number of characters from the String
//                  using recursion.
//  Input         : String
//  Output        : Integer
//  Author        : Sahil Rajaram Thorat
//  Date          : 07/12/2025
//
////////////////////////////////////////////////////////////////////////////////

int Strlen(char str[])
{
    static int iCount = 0;

    if(*str != '\0')
    {
        iCount++;
        str++;
        Strlen(str);
    }

    return iCount;
} // End of Strlen

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    char Arr[20];

    printf("Enter the String : \n");
    scanf("%[^'\n']s", &Arr);

    iRet = Strlen(Arr);           
    
    printf("Number of Characters in the String are : %d\n", iRet);

    return 0;

} // End of main

