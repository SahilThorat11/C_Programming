#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints Numbers & "*" in reverse order using recursion.
//  Input         : Integer
//  Output        : void
//  Author        : Sahil Rajaram Thorat
//  Date          : 07/12/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static int iCount = 0;

    if(iNo != iCount)
    {
        printf("%d\t*\t", iNo);
        Display(iNo - 1);             // Recursive call
    }

} // End of Display

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : \n");
    scanf("%d", &iValue);

    Display(iValue);                

    return 0;

} // End of main

