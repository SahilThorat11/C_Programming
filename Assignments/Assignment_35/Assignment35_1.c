////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[20];
    int iRet = 0;

    printf("Entre the String : \n");
    scanf("%[^'\n']s", Arr);

    iRet = CountCapital(Arr);

    printf("Capital Letters are : %d\n", iRet);

    return 0;
} // End of main

