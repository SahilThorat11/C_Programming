#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Sum
//  Description   : Accept one number and return Summation of its all digits
//                  using recursion.
//  Input         : Integer
//  Output        : Integer
//  Author        : Sahil Rajaram Thorat
//  Date          : 07/12/2025
//
////////////////////////////////////////////////////////////////////////////////

int Sum(int iNo)
{
    static int iDigit = 0;
    static int iSum = 0;

    if(iNo != 0)
    {
        iDigit = iNo % 10;
        iSum = iSum + iDigit;
        iNo = iNo / 10;
        Sum(iNo);
    }

    return iSum;

} // End of Sum

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter the number : \n");
    scanf("%d", &iValue);

    iRet = Sum(iValue);           
    
    printf("Summation of Digits are : %d\n", iRet);

    return 0;

} // End of main

