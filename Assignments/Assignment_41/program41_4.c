#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Fact
//  Description   : Accept one number and return factorial of that number using 
//                  recursion.
//  Input         : Integer
//  Output        : Integer
//  Author        : Sahil Rajaram Thorat
//  Date          : 07/12/2025
//
////////////////////////////////////////////////////////////////////////////////

int Fact(int iNo)
{
    static int i = 1;
    static long lFact = 1;

    if(i <= iNo)
    {
        lFact = lFact * i;
        i++;
        Fact(iNo);
    }

    return lFact;
} // End of Sum

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    long lRet = 0;

    printf("Enter the number : \n");
    scanf("%d", &iValue);

    lRet = Fact(iValue);           
    
    printf("Factorial of number %d is : %d\n", iValue, lRet);

    return 0;

} // End of main

