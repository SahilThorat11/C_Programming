#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints 5    4   3   2   1  using recursion.
//  Input         : None
//  Output        : None
//  Author        : Sahil Rajaram Thorat
//  Date          : 06/12/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static int iCount = 5;     // Counter for recursive calls

    if(iCount > 0)            
    {
        printf("%d\t", iCount);
        iCount--;
        Display();             // Recursive call
    }

} // End of Display

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();                

    return 0;

} // End of main

