#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints Numbers using recursion.
//  Input         : Integer
//  Output        : void
//  Author        : Sahil Rajaram Thorat
//  Date          : 06/12/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    static int iCount = 0;     // Counter for recursive calls

    if(iCount < iNo)            
    {
        printf("%d\t", iCount + 1);
        iCount++;
        Display(iNo);             // Recursive call
    }

} // End of Display

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : \n");
    scanf("%d", &iValue);

    Display(iValue);                

    return 0;

} // End of main

