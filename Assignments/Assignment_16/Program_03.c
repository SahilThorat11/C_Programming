#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : FindFact
//  Description     : program to find factorial of given number
//  Input           : Integer
//  Output          : Integer
//  Author          : Sahil Rajaram Thorat
//  Date            : 10/02/2026
//
////////////////////////////////////////////////////////////////////////////////

int FindFact(int iNo)
{
    int iCnt = 0, iFact = 1;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        iFact = iFact * iCnt; 
    }

    return iFact;
}   // End of FindFact Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    iRet = FindFact(iValue);

    printf("Factorial of number %d are : %d\n", iValue, iRet);

    return 0;
}   // End of main Function

