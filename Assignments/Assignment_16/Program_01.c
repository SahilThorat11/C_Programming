#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : calculateSum
//  Description     : program to calculate the sum of first N natural number
//  Input           : Integer
//  Output          : Integer
//  Author          : Sahil Rajaram Thorat
//  Date            : 10/02/2026
//
////////////////////////////////////////////////////////////////////////////////

int CalculateSum(int iNo)
{
    int iCnt = 0, iSum = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        iSum = iSum + iCnt; 
    }

    return iSum;
}   // End of CalculateSum Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    iRet = CalculateSum(iValue);

    printf("Summation of all natural numbers till %d are : %d\n", iValue, iRet);

    return 0;
}   // End of main Function

