#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Mult
//  Description   : Accept one number and return product of its all digits
//                  using recursion.
//  Input         : Integer
//  Output        : Integer
//  Author        : Sahil Rajaram Thorat
//  Date          : 07/12/2025
//
////////////////////////////////////////////////////////////////////////////////

int Mult(int iNo)
{
    static int iDigit = 0;
    static int iMul = 1;

    if(iNo != 0)
    {
        iDigit = iNo % 10;
        iMul = iMul * iDigit;
        iNo = iNo / 10;
        Mult(iNo);
    }

    return iMul;

} // End of Mult

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter the number : \n");
    scanf("%d", &iValue);

    iRet = Mult(iValue);           
    
    printf("Product of Digits are : %d\n", iRet);

    return 0;

} // End of main

