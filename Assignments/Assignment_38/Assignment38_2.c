#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : strCpyX
//  Description   : Accept String from user & copy that characters of that string
//                  into another string by removing all white spaces.
//  Input         : Character
//  Output        : void
//  Author        : Sahil Rajaram Thorat
//  Date          : 03/12/2025
//
////////////////////////////////////////////////////////////////////////////////

void strCpyX(char *str, char *dest)
{
    while (*str != '\0')
    {
        if (*str != ' ')
        {
            *dest = *str;
            dest++;
        }
        str++;
    }
    *dest = '\0';

} // End of function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[30];
    char Brr[30];

    printf("Enter the String :\n");
    scanf("%[^\n]", Arr);

    strCpyX(Arr, Brr);

    printf("%s", Brr);

    return 0;

} // End of main

