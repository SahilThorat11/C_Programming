#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Prints  a   b   c   d   e   f  using recursion.
//  Input         : None
//  Output        : None
//  Author        : Sahil Rajaram Thorat
//  Date          : 06/12/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static char ch = 'a';     // Counter for recursive calls

    if(ch <= 'f')            
    {
        printf("%c\t", ch);
        ch++;
        Display();             // Recursive call
    }

} // End of Display

////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function: main
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();                

    return 0;

} // End of main

