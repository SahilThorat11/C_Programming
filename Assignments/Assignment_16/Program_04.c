#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : RevNumber
//  Description     : program to Reverse the given number
//  Input           : Integer
//  Output          : Integer
//  Author          : Sahil Rajaram Thorat
//  Date            : 10/02/2026
//
////////////////////////////////////////////////////////////////////////////////

int RevNumber(int iNo)
{
    int iDigit = 0, iRev = 0;;

    while(iNo != 0)
    {
        iDigit = iNo % 10;
        iRev = (iRev * 10) + iDigit;
        iNo = iNo / 10;
    }

    return iRev;
    
}   // End of RevNumber Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point Function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    iRet = RevNumber(iValue);

    printf("Reverse of number %d are : %d\n", iValue, iRet);

    return 0;
}   // End of main Function

